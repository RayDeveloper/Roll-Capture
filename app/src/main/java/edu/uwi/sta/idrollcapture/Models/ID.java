public class ID {

    String slno;
    String name;
    String phone;

    public String getID() {
        return slno;
    }

    public void setID(String slno) {
        this.slno = slno;
    }

    public String getTime() {
        return name;
    }

    public void setTime(String name) {
        this.name = name;
    }


    public String getDate() {
        return phone;
    }

    public void setDate(String name) {
        this.phone = name;
    }


}
